package com.abhay;

public class Variables {
    public static void main(String[] args) {
        //When defining a variable you have to0 specify what type of data type it is.
        //Primitive Data Types vs Abstract data types
        //Check out Primituive vs Reference/Abstract data types.png for more
        // Primitive data types are those supported directly by the language and are not composed of any other data types. Kind of like they are atomic that way. The language has support for operations on those data types already.
        // Abstract data types are data structures of holds that the user defines. Example :- lists, sets, dictionaries, etc
        // For more data types go to datatypes.png
        int x = 7;
        System.out.println(x);
        //above is an INT variable
        char charmander = 'c';
        System.out.println(charmander);
        //above 'char' means character - so any ONE character on the keyboard
        //obviously there are more so yea.
        char symbol = '@';
        String myname = "ABHAY";
        System.out.println(symbol + myname);


    }
}
