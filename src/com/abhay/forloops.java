package com.abhay;

public class forloops {
    public static void main(String[] args){
        //for loop == executes a block of code a certain amount of times.
        // 3 things go in the parenthesis below.
        // 1. Initializer == (It creates temporary variables. Creating)
        // 2. Condition ==  (THE condition for it to run on.)
        // 3. Updates the loop. IT modifies the value of the for loop to break it at some time.
        //WE NEED A SemiColon in between not a comma.
        //For number 3 we can als say x = x+1 | x+=1
        for(int x = 10; x>=0; x--) {
            System.out.println(x);
        }
        System.out.println("happy new year!");
        }
}
